package Q13;

public class firstForm {
	private int d;
	private int e;
	private int k;
	private int l;

	firstForm(int d,int  e,int  k,int  l) {
		this.d = d;
		this.e = e;
		this.k = k;
		this.l = l;
	}

	public int getD() {
		return d;
	}

	public int getE() {
		return e;
	}

	public int getK() {
		return k;
	}

	public int getL() {
		return l;
	}

}
