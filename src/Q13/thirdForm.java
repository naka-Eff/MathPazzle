package Q13;

public class thirdForm {
	private int r;
	private int e;
	private int a;
	private int d;

	private int w;
	private int i;
	private int t;
	private int l;
	private int k;
	private int s;

	public thirdForm(int r, int e, int a, int d, int w, int i, int t, int l, int k, int s) {
		this.r = r;
		this.e = e;
		this.a = a;
		this.d = d;
		this.w = w;
		this.i = i;
		this.t = t;
		this.l = l;
		this.k = k;
		this.s = s;
	}

	public void print() {
		System.out.println("r:" + r + " e:" + e + " a:" + a + " d:" + d + " w:" + w + " i:" + i + " t:" + t + " l:" + l
				+ " k:" + k + " s:" + s);
	}

	public int getR() {
		return r;
	}

	public void setR(int r) {
		this.r = r;
	}

	public int getE() {
		return e;
	}

	public void setE(int e) {
		this.e = e;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getD() {
		return d;
	}

	public void setD(int d) {
		this.d = d;
	}

	public int getW() {
		return w;
	}

	public void setW(int w) {
		this.w = w;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public int getT() {
		return t;
	}

	public void setT(int t) {
		this.t = t;
	}

	public int getL() {
		return l;
	}

	public void setL(int l) {
		this.l = l;
	}

	public int getK() {
		return k;
	}

	public void setK(int k) {
		this.k = k;
	}

	public int getS() {
		return s;
	}

	public void setS(int s) {
		this.s = s;
	}

}
